export async function transitionTournamentRoundStatus(
  tournamentID: string,
  roundID: string,
  status: TournamentRoundRecord['status'],
  options: { override?: boolean } = {},
) {
  const rounds = await getNormalizedTournamentRounds(tournamentID)
  const currentIndex = rounds.findIndex(([currentRoundID]) => currentRoundID === roundID)
  if (currentIndex < 0) {
    throw new Error('Tournament round not found')
  }

  const currentRound = rounds[currentIndex][1]
  const previousRounds = rounds.slice(0, currentIndex)
  const previousRoundIncomplete = previousRounds.find(([, round]) => round.status !== 'completed' && round.status !== 'archived')
  if ((status === 'ready' || status === 'active') && previousRoundIncomplete && !options.override) {
    throw new Error(`Complete ${previousRoundIncomplete[1].title} before unlocking this round`)
  }

  if (status === 'completed' && !options.override) {
    const completionState = await getRoundCompletionState(tournamentID, roundID)
    if (!completionState.isReadyToAdvance) {
      throw new Error('All linked matches and schedule blocks must be completed before advancing the round')
    }
  }

  const nextRound = await updateTournamentRound(tournamentID, roundID, {
    status,
    unlockedAt: status === 'ready' ? (currentRound.unlockedAt || new Date().toISOString()) : currentRound.unlockedAt,
  })
  const scheduleBlocks = await getNormalizedTournamentScheduleBlocks(tournamentID)

  await Promise.all(scheduleBlocks
    .filter(([, scheduleBlock]) => scheduleBlock.roundID === roundID)
    .map(async ([scheduleBlockID]) => {
      await updateTournamentScheduleBlock(tournamentID, scheduleBlockID, {
        status: getTournamentRoundStatusBlockStatus(status),
      })
    }))

  if (status === 'completed') {
    await autoAdvanceRoundWinners(tournamentID, roundID, nextRound, rounds)
    await unlockNextTournamentRound(tournamentID, roundID, rounds)
  }

  return nextRound
}

export async function setTournamentRoundMatchAssignments(
  tournamentID: string,
  roundID: string,
  matchIDs: string[],
) {
  const snapshot = await db.ref(`tournaments/${tournamentID}/rounds/${roundID}`).get()
  const currentRound = snapshot.val()
  if (!currentRound || typeof currentRound !== 'object') {
    throw new Error('Tournament round not found')
  }

  const normalizedMatchIDs = Array.from(new Set(matchIDs.map((matchID) => matchID.trim()).filter(Boolean)))
  const currentRoundRecord = normalizeTournamentRoundRecord(currentRound as TournamentRoundRecord)
  const removedMatchIDs = currentRoundRecord.assignedMatchIDs.filter((matchID) => !normalizedMatchIDs.includes(matchID))
  const allRounds = await getNormalizedTournamentRounds(tournamentID)
  const scheduleBlocks = await getNormalizedTournamentScheduleBlocks(tournamentID)

  await Promise.all(allRounds
    .filter(([currentRoundID, round]) => currentRoundID !== roundID && round.assignedMatchIDs.some((matchID) => normalizedMatchIDs.includes(matchID)))
    .map(async ([currentRoundID, round]) => {
      await updateTournamentRound(tournamentID, currentRoundID, {
        assignedMatchIDs: round.assignedMatchIDs.filter((matchID) => !normalizedMatchIDs.includes(matchID)),
      })
    }))

  const nextRound = await updateTournamentRound(tournamentID, roundID, {
    assignedMatchIDs: normalizedMatchIDs,
  })

  await Promise.all(removedMatchIDs.map(async (matchID) => {
    const stillLinkedByScheduleBlock = scheduleBlocks.some(([, scheduleBlock]) => scheduleBlock.roundID === roundID && scheduleBlock.sourceMatchID === matchID)
    if (stillLinkedByScheduleBlock) {
      return
    }
    const matchSnapshot = await db.ref(`matches/${matchID}`).get()
    const match = matchSnapshot.val()
    if (!match || typeof match !== 'object') {
      return
    }
    await db.ref(`matches/${matchID}`).update({
      roundID: '',
      matchRound: '',
      tournamentContext: {
        ...((match.tournamentContext && typeof match.tournamentContext === 'object') ? match.tournamentContext : (match.context && typeof match.context === 'object') ? match.context : {}),
        roundID: '',
        matchRound: '',
      },
      context: {
        ...((match.context && typeof match.context === 'object') ? match.context : (match.tournamentContext && typeof match.tournamentContext === 'object') ? match.tournamentContext : {}),
        roundID: '',
        matchRound: '',
      },
    })
  }))

  await Promise.all(normalizedMatchIDs.map(async (matchID) => {
    const matchSnapshot = await db.ref(`matches/${matchID}`).get()
    const match = matchSnapshot.val()
    if (!match || typeof match !== 'object') {
      return
    }
    await db.ref(`matches/${matchID}`).update({
      tournamentID,
      eventID: nextRound.eventID || String(match.eventID || ''),
      roundID,
      matchRound: nextRound.title,
      tournamentContext: {
        ...((match.tournamentContext && typeof match.tournamentContext === 'object') ? match.tournamentContext : (match.context && typeof match.context === 'object') ? match.context : {}),
        tournamentID,
        eventID: nextRound.eventID || String(match.eventID || ''),
        roundID,
        matchRound: nextRound.title,
      },
      context: {
        ...((match.context && typeof match.context === 'object') ? match.context : (match.tournamentContext && typeof match.tournamentContext === 'object') ? match.tournamentContext : {}),
        tournamentID,
        eventID: nextRound.eventID || String(match.eventID || ''),
        roundID,
        matchRound: nextRound.title,
      },
    })
  }))

  return nextRound
}

export async function bulkAssignTournamentRoundToTables(
  tournamentID: string,
  roundID: string,
  tableIDs: string[],
  tableLabelMap: Record<string, string> = {},
) {
  const snapshot = await db.ref(`tournaments/${tournamentID}/rounds/${roundID}`).get()
  const currentRound = snapshot.val()
  if (!currentRound || typeof currentRound !== 'object') {
    throw new Error('Tournament round not found')
  }

  const normalizedTableIDs = Array.from(new Set(tableIDs.map((tableID) => tableID.trim()).filter(Boolean)))
  const nextRound = await updateTournamentRound(tournamentID, roundID, {
    assignedTableIDs: normalizedTableIDs,
    assignedTableLabels: normalizedTableIDs.map((tableID) => tableLabelMap[tableID] || ''),
  })
  const scheduleBlocks = (await getNormalizedTournamentScheduleBlocks(tournamentID))
    .filter(([, scheduleBlock]) => scheduleBlock.roundID === roundID)
    .sort((a, b) => new Date(a[1].scheduledStartTime || 0).getTime() - new Date(b[1].scheduledStartTime || 0).getTime())

  if (normalizedTableIDs.length === 0) {
    return nextRound
  }

  for (const [index, [scheduleBlockID, scheduleBlock]] of scheduleBlocks.entries()) {
    const assignedTableID = normalizedTableIDs[index % normalizedTableIDs.length]
    await updateTournamentScheduleBlock(tournamentID, scheduleBlockID, {
      assignedTableID,
      assignedTableLabel: tableLabelMap[assignedTableID] || '',
      scheduledStartTime: scheduleBlock.scheduledStartTime || nextRound.scheduledStartTime || '',
      status: scheduleBlock.blockType === 'match' && scheduleBlock.sourceMatchID ? 'scheduled' : scheduleBlock.status,
    })
    if (scheduleBlock.blockType === 'match' && (scheduleBlock.sourceMatchID || scheduleBlock.assignedQueueItemID)) {
      await syncTournamentScheduleBlockToQueue(tournamentID, scheduleBlockID)
    }
  }

  return nextRound
}

export async function syncTournamentRoundAutomation(
  tournamentID: string,
  roundID: string,
) {
  const completionState = await getRoundCompletionState(tournamentID, roundID)
  if (!completionState.round) {
    return null
  }

  if (!completionState.isReadyToAdvance || !completionState.round.advanceWhenComplete) {
    return completionState.round
  }

  if (completionState.round.status !== 'completed') {
    return transitionTournamentRoundStatus(tournamentID, roundID, 'completed', {
      override: true,
    })
  }

  await autoAdvanceRoundWinners(tournamentID, roundID, completionState.round)
  await unlockNextTournamentRound(tournamentID, roundID)
  return completionState.round
}

export async function addTournamentScheduleBlock(tournamentID: string, input: {
  title: string
  blockType?: TournamentScheduleBlockType
  eventID?: string
  roundID?: string
  sourceMatchID?: string
  eventName?: string
  roundTitle?: string
  scheduledStartTime?: string
  scheduledEndTime?: string
  participantKeys?: string[]
  participantLabels?: string[]
  isPublished?: boolean
  assignedTableID?: string
  assignedTableLabel?: string
  notes?: string
  isAutoGenerated?: boolean
  generatedFromRoundID?: string
  generatedOrder?: number
}) {
  const blockType = input.blockType || 'match'
  const scheduledStartTime = input.scheduledStartTime || ''
  const scheduledEndTime = input.scheduledEndTime || getTournamentScheduleBlockEndTime({
    blockType,
    scheduledStartTime,
    scheduledEndTime: '',
  })
  const scheduleBlock = normalizeTournamentScheduleBlockRecord({
    blockType,
    title: input.title,
    eventID: input.eventID || '',
    roundID: input.roundID || '',
    sourceMatchID: input.sourceMatchID || '',
    eventName: input.eventName || '',
    roundTitle: input.roundTitle || '',
    scheduledStartTime,
    scheduledEndTime,
    participantKeys: input.participantKeys || [],
    participantLabels: input.participantLabels || [],
    hasConflicts: false,
    conflictingBlockIDs: [],
    conflictingParticipantLabels: [],
    isPublished: input.isPublished !== false,
    publishedAt: input.isPublished === false ? '' : new Date().toISOString(),
    assignedTableID: input.assignedTableID || '',
    assignedTableLabel: input.assignedTableLabel || '',
    assignedQueueItemID: '',
    status: blockType === 'match' && input.assignedTableID ? 'scheduled' : 'unassigned',
    notes: input.notes || '',
    isAutoGenerated: Boolean(input.isAutoGenerated),
    generatedFromRoundID: input.generatedFromRoundID || '',
    generatedOrder: input.generatedOrder || 0,
    createdAt: new Date().toISOString(),
    updatedAt: new Date().toISOString(),
  })
  const pushedScheduleBlock = await db.ref(`tournaments/${tournamentID}/scheduleBlocks`).push(scheduleBlock)
  await updateTournament(tournamentID, {})
  await detectTournamentScheduleConflicts(tournamentID)
  return pushedScheduleBlock.key
}

export async function createTournamentScheduleMatch(tournamentID: string, scheduleBlockID: string, sportName = 'tableTennis', scoringType: string | null = 'normal') {
  const snapshot = await db.ref(`tournaments/${tournamentID}/scheduleBlocks/${scheduleBlockID}`).get()
  const currentScheduleBlock = snapshot.val()
  if (!currentScheduleBlock || typeof currentScheduleBlock !== 'object') {
    throw new Error('Tournament schedule block not found')
  }

  const scheduleBlock = currentScheduleBlock as TournamentScheduleBlockRecord
  if (scheduleBlock.blockType !== 'match') {
    throw new Error('Only match schedule blocks can create tournament matches')
  }
  if (scheduleBlock.sourceMatchID) {
    return scheduleBlock.sourceMatchID
  }

  const eventRecord = scheduleBlock.eventID
    ? (await db.ref(`tournaments/${tournamentID}/events/${scheduleBlock.eventID}`).get()).val() as TournamentEventRecord | null
    : null
  const roundRecord = scheduleBlock.roundID
    ? (await db.ref(`tournaments/${tournamentID}/rounds/${scheduleBlock.roundID}`).get()).val() as TournamentRoundRecord | null
    : null

  const newMatch = new Match().createNew(sportName, {
    tournamentID,
    eventID: scheduleBlock.eventID || '',
    roundID: scheduleBlock.roundID || '',
    matchRound: scheduleBlock.roundTitle || roundRecord?.title || '',
    eventName: scheduleBlock.eventName || eventRecord?.name || '',
    scoringType,
    sportName,
  }, false, scoringType)

  const pushedMatch = await db.ref('matches').push(newMatch)
  await updateTournamentScheduleBlock(tournamentID, scheduleBlockID, {
    sourceMatchID: pushedMatch.key || '',
    eventName: scheduleBlock.eventName || eventRecord?.name || '',
    roundTitle: scheduleBlock.roundTitle || roundRecord?.title || '',
  })
  return pushedMatch.key
}

export async function updateTournamentScheduleBlock(
  tournamentID: string,
  scheduleBlockID: string,
  patch: Partial<TournamentScheduleBlockRecord>,
) {
  const snapshot = await db.ref(`tournaments/${tournamentID}/scheduleBlocks/${scheduleBlockID}`).get()
  const currentScheduleBlock = snapshot.val()
  if (!currentScheduleBlock || typeof currentScheduleBlock !== 'object') {
    throw new Error('Tournament schedule block not found')
  }
  const previousScheduleBlock = normalizeTournamentScheduleBlockRecord(currentScheduleBlock as TournamentScheduleBlockRecord)
  const nextScheduleBlock = normalizeTournamentScheduleBlockRecord({
    ...previousScheduleBlock,
    ...patch,
    publishedAt: patch.isPublished === false
      ? ''
      : patch.isPublished === true
          ? String(previousScheduleBlock.publishedAt || new Date().toISOString())
          : previousScheduleBlock.publishedAt,
    updatedAt: new Date().toISOString(),
  })
  nextScheduleBlock.scheduledEndTime = getTournamentScheduleBlockEndTime(nextScheduleBlock)
  if (
    previousScheduleBlock.assignedQueueItemID
    && previousScheduleBlock.assignedTableID
    && (
      nextScheduleBlock.blockType !== 'match'
      || !nextScheduleBlock.assignedTableID
      || !nextScheduleBlock.sourceMatchID
    )
  ) {
    await deleteScheduledTableMatch(previousScheduleBlock.assignedTableID, previousScheduleBlock.assignedQueueItemID)
    nextScheduleBlock.assignedQueueItemID = ''
  }
  if (nextScheduleBlock.blockType !== 'match') {
    nextScheduleBlock.status = ['completed', 'cancelled'].includes(nextScheduleBlock.status) ? nextScheduleBlock.status : 'unassigned'
    nextScheduleBlock.assignedQueueItemID = ''
  }
  await db.ref(`tournaments/${tournamentID}/scheduleBlocks/${scheduleBlockID}`).set(nextScheduleBlock)
  if (nextScheduleBlock.sourceMatchID) {
    const [matchSnapshot, roundSnapshot] = await Promise.all([
      db.ref(`matches/${nextScheduleBlock.sourceMatchID}`).get(),
      nextScheduleBlock.roundID ? db.ref(`tournaments/${tournamentID}/rounds/${nextScheduleBlock.roundID}`).get() : Promise.resolve({ val: () => null } as { val: () => null }),
    ])
    const match = matchSnapshot.val()
    const round = normalizeTournamentRoundRecord(roundSnapshot.val() as TournamentRoundRecord | null)
    if (match && typeof match === 'object') {
      await db.ref(`matches/${nextScheduleBlock.sourceMatchID}`).update({
        tournamentID,
        eventID: nextScheduleBlock.eventID || String(match.eventID || ''),
        roundID: nextScheduleBlock.roundID || '',
        matchRound: nextScheduleBlock.roundTitle || round.title || '',
        eventName: nextScheduleBlock.eventName || String(match.eventName || ''),
        tournamentContext: {
          ...((match.tournamentContext && typeof match.tournamentContext === 'object') ? match.tournamentContext : (match.context && typeof match.context === 'object') ? match.context : {}),
          tournamentID,
          eventID: nextScheduleBlock.eventID || String(match.eventID || ''),
          roundID: nextScheduleBlock.roundID || '',
          matchRound: nextScheduleBlock.roundTitle || round.title || '',
          eventName: nextScheduleBlock.eventName || String(match.eventName || ''),
        },
        context: {
          ...((match.context && typeof match.context === 'object') ? match.context : (match.tournamentContext && typeof match.tournamentContext === 'object') ? match.tournamentContext : {}),
          tournamentID,
          eventID: nextScheduleBlock.eventID || String(match.eventID || ''),
          roundID: nextScheduleBlock.roundID || '',
          matchRound: nextScheduleBlock.roundTitle || round.title || '',
          eventName: nextScheduleBlock.eventName || String(match.eventName || ''),
        },
      })
    }
  }
  if (nextScheduleBlock.roundID && ['completed', 'cancelled'].includes(nextScheduleBlock.status)) {
    await syncTournamentRoundAutomation(tournamentID, nextScheduleBlock.roundID)
  }
  await detectTournamentScheduleConflicts(tournamentID)
  return nextScheduleBlock
}

export async function deleteTournamentScheduleBlock(tournamentID: string, scheduleBlockID: string) {
  const snapshot = await db.ref(`tournaments/${tournamentID}/scheduleBlocks/${scheduleBlockID}`).get()
  const currentScheduleBlock = snapshot.val()
  if (currentScheduleBlock && typeof currentScheduleBlock === 'object') {
    const scheduleBlock = normalizeTournamentScheduleBlockRecord(currentScheduleBlock as TournamentScheduleBlockRecord)
    if (scheduleBlock.assignedTableID && scheduleBlock.assignedQueueItemID) {
      await deleteScheduledTableMatch(scheduleBlock.assignedTableID, scheduleBlock.assignedQueueItemID)
    }
  }
  await db.ref(`tournaments/${tournamentID}/scheduleBlocks/${scheduleBlockID}`).remove()
  await detectTournamentScheduleConflicts(tournamentID)
}

export async function queueTournamentScheduleBlock(tournamentID: string, scheduleBlockID: string) {
  const tournament = await getTournament(tournamentID)
  const snapshot = await db.ref(`tournaments/${tournamentID}/scheduleBlocks/${scheduleBlockID}`).get()
  const currentScheduleBlock = snapshot.val()
  if (!tournament || !currentScheduleBlock || typeof currentScheduleBlock !== 'object') {
    throw new Error('Tournament schedule block not found')
  }

  const scheduleBlock = currentScheduleBlock as TournamentScheduleBlockRecord
  if (scheduleBlock.blockType !== 'match') {
    throw new Error('Only match schedule blocks can be queued to tables')
  }
  if (!scheduleBlock.assignedTableID || !scheduleBlock.sourceMatchID) {
    throw new Error('Schedule block needs both a source match and assigned table')
  }

  const eventRecord = scheduleBlock.eventID
    ? (await db.ref(`tournaments/${tournamentID}/events/${scheduleBlock.eventID}`).get()).val() as TournamentEventRecord | null
    : null
  const roundRecord = scheduleBlock.roundID
    ? (await db.ref(`tournaments/${tournamentID}/rounds/${scheduleBlock.roundID}`).get()).val() as TournamentRoundRecord | null
    : null

  const eventName = scheduleBlock.eventName || eventRecord?.name || ''
  const roundTitle = scheduleBlock.roundTitle || roundRecord?.title || ''

  await db.ref(`matches/${scheduleBlock.sourceMatchID}`).update({
    tournamentID,
    eventID: scheduleBlock.eventID || '',
    roundID: scheduleBlock.roundID || '',
    eventName,
    matchRound: roundTitle,
  })
  await db.ref(`matches/${scheduleBlock.sourceMatchID}/scheduling`).update({
    tableID: scheduleBlock.assignedTableID,
    scheduledStartTime: scheduleBlock.scheduledStartTime || '',
    sourceType: 'tournament-schedule',
    sourceID: scheduleBlockID,
  })

  const queueItemID = await addScheduledMatch(scheduleBlock.assignedTableID, scheduleBlock.sourceMatchID, scheduleBlock.scheduledStartTime || '', {
    tournamentID,
    eventID: scheduleBlock.eventID || '',
    roundID: scheduleBlock.roundID || '',
    eventName,
    matchRound: roundTitle,
    sourceType: 'tournament-schedule',
    sourceID: scheduleBlockID,
    operatorNotes: scheduleBlock.notes || '',
  })

  await updateTournamentScheduleBlock(tournamentID, scheduleBlockID, {
    eventName,
    roundTitle,
    assignedQueueItemID: queueItemID || '',
    status: queueItemID ? 'scheduled' : scheduleBlock.status,
  })

  return queueItemID
}

export async function syncTournamentScheduleBlockToQueue(tournamentID: string, scheduleBlockID: string) {
  const snapshot = await db.ref(`tournaments/${tournamentID}/scheduleBlocks/${scheduleBlockID}`).get()
  const currentScheduleBlock = snapshot.val()
  if (!currentScheduleBlock || typeof currentScheduleBlock !== 'object') {
    throw new Error('Tournament schedule block not found')
  }

  const scheduleBlock = currentScheduleBlock as TournamentScheduleBlockRecord
  if (scheduleBlock.blockType !== 'match') {
    throw new Error('Only match schedule blocks can sync to a table queue')
  }
  if (!scheduleBlock.assignedTableID || !scheduleBlock.sourceMatchID) {
    throw new Error('Schedule block needs both a source match and assigned table')
  }

  const eventRecord = scheduleBlock.eventID
    ? (await db.ref(`tournaments/${tournamentID}/events/${scheduleBlock.eventID}`).get()).val() as TournamentEventRecord | null
    : null
  const roundRecord = scheduleBlock.roundID
    ? (await db.ref(`tournaments/${tournamentID}/rounds/${scheduleBlock.roundID}`).get()).val() as TournamentRoundRecord | null
    : null

  const eventName = scheduleBlock.eventName || eventRecord?.name || ''
  const roundTitle = scheduleBlock.roundTitle || roundRecord?.title || ''
  const queuePatch = {
    tournamentID,
    eventID: scheduleBlock.eventID || '',
    roundID: scheduleBlock.roundID || '',
    eventName,
    matchRound: roundTitle,
    sourceType: 'tournament-schedule',
    sourceID: scheduleBlockID,
    operatorNotes: scheduleBlock.notes || '',
  }

  if (!scheduleBlock.assignedQueueItemID) {
    return queueTournamentScheduleBlock(tournamentID, scheduleBlockID)
  }

  const sourceTableID = scheduleBlock.assignedTableID
  const queueItemSnapshot = await db.ref(`tables/${sourceTableID}/scheduledMatches/${scheduleBlock.assignedQueueItemID}`).get()
  if (queueItemSnapshot.val()) {
    await updateScheduledMatch(sourceTableID, scheduleBlock.assignedQueueItemID, scheduleBlock.sourceMatchID, scheduleBlock.scheduledStartTime || '', queuePatch)
    await updateTournamentScheduleBlock(tournamentID, scheduleBlockID, {
      eventName,
      roundTitle,
      status: 'scheduled',
    })
    return scheduleBlock.assignedQueueItemID
  }

  const allTables = await db.ref('tables').get()
  const allTableRecords = allTables.val() && typeof allTables.val() === 'object'
    ? allTables.val() as Record<string, Record<string, unknown>>
    : {}
  const currentTableEntry = Object.entries(allTableRecords).find(([, tableRecord]) => {
    const scheduledMatches = tableRecord?.scheduledMatches && typeof tableRecord.scheduledMatches === 'object'
      ? tableRecord.scheduledMatches as Record<string, Record<string, unknown>>
      : {}
    return Boolean(scheduledMatches[scheduleBlock.assignedQueueItemID])
  })

  if (currentTableEntry) {
    const [currentTableID] = currentTableEntry
    if (currentTableID !== sourceTableID) {
      const movedQueueItemID = await moveScheduledTableMatchToTable(currentTableID, sourceTableID, scheduleBlock.assignedQueueItemID)
      await updateTournamentScheduleBlock(tournamentID, scheduleBlockID, {
        assignedQueueItemID: movedQueueItemID || '',
      })
      if (movedQueueItemID) {
        await updateScheduledMatch(sourceTableID, movedQueueItemID, scheduleBlock.sourceMatchID, scheduleBlock.scheduledStartTime || '', queuePatch)
      }
      return movedQueueItemID
    }
  }

  const queueItemID = await addScheduledMatch(sourceTableID, scheduleBlock.sourceMatchID, scheduleBlock.scheduledStartTime || '', queuePatch)
  await updateTournamentScheduleBlock(tournamentID, scheduleBlockID, {
    eventName,
    roundTitle,
    assignedQueueItemID: queueItemID || '',
    status: queueItemID ? 'scheduled' : scheduleBlock.status,
  })
  return queueItemID
}

export async function reorderTournamentRound(
  tournamentID: string,
  roundID: string,
  direction: 'up' | 'down',
) {
  const orderedRounds = await getNormalizedTournamentRounds(tournamentID)
  if (orderedRounds.length === 0) {
    return []
  }
  const currentIndex = orderedRounds.findIndex(([currentRoundID]) => currentRoundID === roundID)
  if (currentIndex < 0) {
    return orderedRounds
  }

  const targetIndex = direction === 'up' ? currentIndex - 1 : currentIndex + 1
  if (targetIndex < 0 || targetIndex >= orderedRounds.length) {
    return orderedRounds
  }

  const reorderedRounds = [...orderedRounds]
  const [movedRound] = reorderedRounds.splice(currentIndex, 1)
  reorderedRounds.splice(targetIndex, 0, movedRound)

  await db.ref(`tournaments/${tournamentID}/rounds`).set(
    Object.fromEntries(reorderedRounds.map(([currentRoundID, round], index) => [
      currentRoundID,
      {
        ...round,
        order: index + 1,
        updatedAt: new Date().toISOString(),
      },
    ])),
  )

  return reorderedRounds
}
